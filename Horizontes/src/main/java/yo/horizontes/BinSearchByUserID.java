    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int a, p = Integer.parseInt(jTextField2.getText()), er = p;
        Persona[] array = new Persona[Integer.parseInt(jTextField1.getText())];
        for (int i = 0; i < array.length; i++)
            array[i] = new Persona("Ciaran" + i, "Anghel" + i, (int) Math.random()*20+1);
        Arrays.sort(array);
        a = array.length / 2;
        
        for (; er > 0; er--) {
            if (p == array[a].getId()) {
                break;
            }
            else if (p >= array[a].getId()) {
                a = a + a / 2;
            }
            else {
                a = a - a / 2;
            }
            if (a > array.length) break;
        }
        if (er != 0) {
            jLabel3.setText("La persona que busca ha sido encontrada. Su nombre es " + 
                    array[a].getNombre() + array[a].getApellido() + "Y su posici√≥n es " + a);
        }
        else {
            jLabel3.setText("La persona no fue encontrada.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

